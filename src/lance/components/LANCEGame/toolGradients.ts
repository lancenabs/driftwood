// First hex of a tool's gradient = its accent color.
export function toolAccentColor(toolId: string): string {
  const g = TOOL_GRADIENTS[toolId];
  return g?.match(/#[0-9A-Fa-f]{6}/)?.[0] ?? '#3ECFCF';
}
export function toolGradient(toolId: string): string {
  return TOOL_GRADIENTS[toolId] ?? 'linear-gradient(145deg, #94A3B8, #64748B)';
}
